fibo<-function(n,f=NULL){
  if(is.null(f)) f<-c(0,1) else f
  
  if(length(f)== n+1){
    return(f)
  }
  else{
    l<-length(f)
    x<-f[l]+f[l-1]
    f<-c(f,x)
    return(fibo(n,f))
  }
}
